package com.company;

public class Airplane extends Vehicle {
    private String flight;

    public String getFlight() {
        return flight;
    }
    public void setFlight(String newFlight) {
        flight = newFlight;
    }

    public void communicate() {
        System.out.println("Tower, this is flight " + flight + ". Requesting permission to land.");
    }

    public void toStr() {
        System.out.println("This is an airplane, flight number " + flight + ".");
    }


}
