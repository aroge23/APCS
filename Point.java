package com.company;

public class Point {
    //Instance Variables
    double x, y;

    //Getters and Setters
    public double getX() {
        return x;
    }

    public void setX(double newX) {
        x = newX;
    }

    public double getY() {
        return y;
    }

    public void setY(double newY) {
        y = newY;
    }

    public Point(double nx, double ny) {
        x = nx;
        y = ny;
    }

    public String toStr() {
        return ("(" + x + ", " + y + ")");
    }
}//end class Point
